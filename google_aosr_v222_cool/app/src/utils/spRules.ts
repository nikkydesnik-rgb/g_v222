/**
 * Suggest SP based on work name keywords
 */
export function suggestSP(workName: string): string {
  if (!workName) return '';

  const lowerWorkName = workName.toLowerCase();

  for (const rule of spRulesDatabase) {
    for (const keyword of rule.keywords) {
      if (lowerWorkName.includes(keyword.toLowerCase())) {
        return rule.sp;
      }
    }
  }

  return '';
}

/**
 * Get all keyword-based rules
 */
export function getAllSPRules(): SPRule[] {
  return spRulesDatabase;
}

/**
 * Parse SP list from file content (one SP per line)
 */
export function parseSPList(content: string): string[] {
  return content
    .split('\n')
    .map((line) => line.trim())
    .filter((line) => line.length > 0);
}
